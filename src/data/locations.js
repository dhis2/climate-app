export const findLocation = (id, orgUnit = locations) => {
    if (orgUnit.id === id) {
        return orgUnit
    } else if (orgUnit.children) {
        let result = null
        for (let i = 0; result == null && i < orgUnit.children.length; i++) {
            result = findLocation(id, orgUnit.children[i])
        }
        return result
    }
    return null
}
